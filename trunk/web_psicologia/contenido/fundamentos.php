<div id="conten">
	<div style="font-size: 11pt;" id="sefora">
		<p>Los <span style="text-decoration: underline;">Fundamentos</span> de mi metodología con Coaching estan basados en el modelo de trabajo denominado C.A.R. ('coche' en ingles), cuyo procedimiento de trabajo comprende estas tres fases primordiales:</p>
		<ul style="list-style-image: url(&quot;images/bolo2.gif&quot;);">
			<li><strong>C</strong>onciencia</li>
			<li><strong>A</strong>utocreencia</li>
			<li><strong>R</strong>esponsabilidad</li>
		</ul>
		<p>La utilidad de un Proceso de Cambio Personal es que la persona alcance el objetivo ( d ) -de destino- que se ha propuesto, partiendo de su situación actual ( o ) -de origen.</p>
		<p>El 'coche' (C.A.R.) será el medio que le llevara desde ( o ) hasta ( d ).</p>
		<p>La persona que va a realizar dicho recorrido, primero necesita tomar <span style="color: rgb(193, 62, 43);" class="ital">Conciencia</span> plena de sí mismo y de su entorno. En esta fase se limpia y aclara el presente de la persona, despojandolo de falsas apariencias, cargas innecesarias y otro tipo de rémoras y bloqueos.</p>
		<p>A continuación estudiará y fortalecerá su <span style="color: rgb(193, 62, 43);" class="ital">Autocreencia</span>. Esto provocará que se abran multiples formas de optar al objetivo, despejando así el camino a seguir de ideas preconcebidas y comportamientos no efectivos.</p>
		<p>Finalmente asumirá y utilizará su <span style="color: rgb(193, 62, 43);" class="ital">Responsabilidad</span>. Esta fase producirá la voluntad y energía necesarias para que la persona inicie el camino elegido y transite por el con seguridad.</p>
