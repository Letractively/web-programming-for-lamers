<div id="conten">
	<div style="font-size: 10.5pt;" id="sefora">
		<p>Para que un <span class="boldi">Proceso de Cambio Personal</span> pueda resultar efectivo, es necesario por parte del cliente un <span style="text-decoration: underline;">compromiso mínimo</span> de 4 sesiones dentro de un plazo máximo de 5 semanas.</p>
		<p>Las sesiones se pueden realizar de tres modos:</p>
		<p style="margin-left: 15px;">
			<span style="text-decoration: underline;">IN SITU</span><br>
			<span style="font-size: 90%;">En esta modalidad las sesiones se realizan en el domicilio personal o profesional del cliente.</span>
		</p>
		<p style="margin-left: 15px;">
			<span style="text-decoration: underline;">PRESENCIAL</span><br>
			<span style="font-size: 90%;">Las sesiones se llevan a cabo en mis gabinetes de trabajo situados en Madrid (centro) y Las Rozas de Madrid.</span>
		</p>		
		<p style="margin-left: 15px;">
			<span style="text-decoration: underline;">A DISTANCIA</span>&nbsp;&nbsp;<a href="index.php?itemid=63"><img alt="Enlace" src="images/link.gif" style="vertical-align: bottom;"></a><br>
			<span style="font-size: 90%;"><b>Por telefono, internet o videoconferencia</b>. Actualmente, y teniendo en cuenta las posibles complicaciones que conllevan la falta de tiempo, los desplazamientos, o una agenda de trabajo en continuo cambio, es una opción muy aconsejable. En esta modalidad el lugar puede ser cualquiera donde se pueda disponer de estos medios de comunicación.</span>
		</p>
		<p>La <span style="text-decoration: underline;">duración</span> de cada sesión es de 60 minutos, aunque podemos acordar, en casos especiales, sesiones de 90 minutos.</p>
		<p>La <span style="text-decoration: underline;">periodicidad</span>, por lo general, es de una sesión por semana, si bien, por causas imprevistas, se pueden hacer cambios para adaptar la agenda de sesiones según convenga.</p>
