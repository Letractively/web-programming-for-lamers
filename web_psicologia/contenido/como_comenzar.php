<p id="titpag">
	Como comenzar
</p>
<div id="conten">
	<div style="font-size: 10.5pt;" id="sefora">
		<p>Para comenzar un <span class="boldi">Proceso de Cambio Personal</span> (PCP), lo primero que se hace es una <span style="text-decoration: underline;">Entrevista Previa</span> de 45 minutos para el conocimiento mutuo y la exposición del asunto. Esta primera sesión es <span style="font-weight: bold; color: rgb(153, 0, 0);">GRATUITA</span>.</p>
		<p>En dicha sesión el/la Cliente me explica brevemente cual es su objetivo, necesidad o inquietud. Después, yo procederé a preguntar una serie de cuestiones para poder evaluar, antes que nada, si lo que necesita es un <span class="boldi">PCP</span>, o por el contrario, necesita los servicios de otro tipo de profesional (abogado, psicólogo, asistente social).</p>
		<p>En caso de que esté bien encaminado hacía el <span class="boldi">PCP</span>, describiré lo siguiente:</p>
		<ul style="list-style-image: url(&quot;images/bolo2.gif&quot;);">
			<li>Como es el <span class="boldi">Proceso de Cambio Personal</span></li>
			<li>Como se desarrolla</li>
			<li>Que debe esperar de mi</li>
			<li>Que se espera de el/ella, para que dicho proceso sea fructífero</li>
		</ul>
		<p>Seguidamente expondré el Código Ético y presentaré el Contrato de Coaching, explicando todos los puntos necesarios, así como honorarios, modalidades, periodos, etc.</p>
		<p>Finalmente, solo queda que el/la Cliente tome la decisión. Si decide que iniciemos su <span class="boldi">Proceso de Cambio Personal</span>, solamente tenemos que firmar dicho contrato y acordar en que fecha iniciarlo y con que horario.</p>
		<p>Este paso lo puede hacer al finalizar la Entrevista Previa, o después, puesto que no existe todavía ningún tipo de compromiso.</p>
	</div>
</div>
